"""Platform routing algorithm for ListIQ.

Given an item description, predicts optimal platform, price tiers, net profit,
sell-through estimates, and a Worth It verdict.

Public API:
    recommend_listing(item: dict) -> dict

The returned dict matches the locked frontend JSON contract exactly.
"""

