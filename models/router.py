# Feature engineering (replicated from notebooks/02_pricing_model.ipynb)
# ---------------------------------------------------------------------------

def _get_brand_tier(brand: str) -> str:
    if brand in LUXURY_BRANDS:
        return "luxury"
    if brand in PREMIUM_BRANDS:
        return "premium"
    if brand != "Unknown":
        return "known"
    return "unknown"


def _engineer_features(df: pd.DataFrame) -> pd.DataFrame:
    """Build all modeling features from raw inputs."""
    out = df.copy()

    # Brand features
    out["brand"] = out["brand"].fillna("Unknown")
    out["brand_tier"] = out["brand"].apply(_get_brand_tier)
    out["brand_top30"] = out["brand"].apply(
        lambda b: b if b in TOP_30_BRANDS else "Other"
    )

    # Condition features
    out["condition"] = out["condition"].fillna("Unknown")
    out["condition_ordinal"] = (
        out["condition"].map(CONDITION_MAP).fillna(0).astype(int)
    )

    # Title features
    title_lower = out["title"].str.lower()
    out["title_length"] = out["title"].str.len()
    out["title_word_count"] = out["title"].str.split().str.len()
    out["title_has_brand"] = (
        (out["brand"] != "Unknown")
        & out.apply(lambda r: r["brand"].lower() in r["title"].lower(), axis=1)
    ).astype(int)
    out["title_has_size"] = title_lower.str.contains(SIZE_PATTERN).astype(int)
    out["title_has_vintage"] = title_lower.str.contains("vintage").astype(int)
    out["title_has_nwt"] = title_lower.str.contains(NWT_PATTERN).astype(int)

    # Interaction features
    out["platform_category"] = out["platform"] + "_" + out["item_category"]

    return out


