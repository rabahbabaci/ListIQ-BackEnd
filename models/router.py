# Public API
# ---------------------------------------------------------------------------

def recommend_listing(item: dict) -> dict:
    """Generate platform recommendations for a resale item.

    Args:
        item: dict with keys:
            - category (str, required): one of 8 valid categories
            - brand (str, optional): brand name, defaults to "Unknown"
            - condition (str, optional): defaults to "Unknown"
            - size (str, optional): display + title synthesis
            - color (str, optional): display + title synthesis
            - estimated_retail (float, optional): display-only

    Returns:
        dict matching the locked frontend JSON contract.
    """
    # --- Input validation ---
    category = item.get("category", "").lower().strip()
    if category not in VALID_CATEGORIES:
        raise ValueError(
            f"Unknown category '{category}'. "
            f"Valid: {', '.join(VALID_CATEGORIES)}"
        )

    brand = item.get("brand") or "Unknown"
    condition = item.get("condition") or "Unknown"
    size = item.get("size")
    color = item.get("color")
    estimated_retail = item.get("estimated_retail")

    title = _synthesize_title(category, brand, size, condition, color)

    # --- Per-platform calculations ---
    raw_recs = []
    for platform in PLATFORMS:
        predicted_price = _predict_price(platform, category, brand,
                                         condition, title)
        price_tiers = _compute_price_tiers(predicted_price, category, platform)
        velocity = _estimate_velocity(category, platform)
        net_profit = _compute_net_profit(price_tiers, platform)
        raw_score = _compute_raw_fit_score(
            net_profit["balanced"],
            velocity["sell_probability_30d"],
            velocity["estimated_days_to_sale"],
        )
        hourly_rate = round(
            net_profit["balanced"] / (SELLER_TIME_MINUTES / 60), 2
        )

        raw_recs.append({
            "platform": platform,
            "price_tiers": price_tiers,
            "platform_fee_pct": PLATFORM_FEES[platform],
            "estimated_shipping": SHIPPING_COST,
            "net_profit": net_profit,
            "estimated_time_minutes": SELLER_TIME_MINUTES,
            "effective_hourly_rate": round(hourly_rate, 2),
            "sell_probability_30d": velocity["sell_probability_30d"],
            "estimated_days_to_sale": velocity["estimated_days_to_sale"],
            "_raw_score": raw_score,
            "_category": category,
        })

    # --- Normalize fit scores to 0-10 ---
    max_score = max(r["_raw_score"] for r in raw_recs)
    for rec in raw_recs:
        if max_score > 0:
            rec["fit_score"] = round(10 * rec["_raw_score"] / max_score, 1)
        else:
            rec["fit_score"] = 0.0

    # --- Rank by fit_score descending ---
    raw_recs.sort(key=lambda r: r["fit_score"], reverse=True)
    for i, rec in enumerate(raw_recs):
        rec["rank"] = i + 1

    # --- Generate reasoning ---
    for rec in raw_recs:
        rec["reasoning"] = _generate_reasoning(rec, raw_recs)

    # --- Build worth_it ---
    worth_it = _build_worth_it(raw_recs)

    # --- Assemble response (strip internal keys) ---
    recommendations = []
    for rec in raw_recs:
        recommendations.append({
            "platform": rec["platform"],
            "rank": rec["rank"],
            "fit_score": rec["fit_score"],
            "price_tiers": rec["price_tiers"],
            "platform_fee_pct": rec["platform_fee_pct"],
            "estimated_shipping": rec["estimated_shipping"],
            "net_profit": rec["net_profit"],
            "estimated_time_minutes": rec["estimated_time_minutes"],
            "effective_hourly_rate": rec["effective_hourly_rate"],
            "sell_probability_30d": rec["sell_probability_30d"],
            "estimated_days_to_sale": rec["estimated_days_to_sale"],
            "reasoning": rec["reasoning"],
        })

    return {
        "item": {
            "category": category,
            "brand": brand,
            "size": size,
            "condition": condition,
            "color": color,
            "estimated_retail": estimated_retail,
        },
        "recommendations": recommendations,
        "worth_it": worth_it,
    }
