# Internal helpers
# ---------------------------------------------------------------------------

def _predict_price(platform: str, category: str, brand: str,
                   condition: str, title: str) -> float:
    """Predict final sale price for an item on a single platform."""
    row = pd.DataFrame([{
        "platform": platform,
        "item_category": category,
        "brand": brand,
        "condition": condition,
        "title": title,
    }])
    row = _engineer_features(row)
    pred_log = _model.predict(row[ALL_FEATURES])
    return float(np.clip(np.expm1(pred_log[0]), 1.0, None))


def _compute_price_tiers(predicted_price: float, category: str,
                         platform: str) -> dict:
    """Compute fast_sale / balanced / max_revenue price tiers."""
    key = (category, platform)
    ratios = PRICE_SPREAD_RATIOS.get(key, PRICE_SPREAD_FALLBACKS.get(platform))
    if ratios is None:
        ratios = {"low": 0.75, "high": 1.5}

    return {
        "fast_sale": round(predicted_price * ratios["low"]),
        "balanced": round(predicted_price),
        "max_revenue": round(predicted_price * ratios["high"]),
    }


def _estimate_velocity(category: str, platform: str) -> dict:
    """Estimate days_to_sale and sell_probability_30d."""
    base_days = POSHMARK_VELOCITY_DAYS.get(category, 30)
    multiplier = PLATFORM_VELOCITY_MULTIPLIERS.get(platform, 1.0)
    days = max(1, round(base_days * multiplier))
    sell_prob = min(1.0, round(30 / days, 2))
    return {
        "estimated_days_to_sale": days,
        "sell_probability_30d": sell_prob,
    }


def _compute_net_profit(price_tiers: dict, platform: str) -> dict:
    """Compute net profit for each price tier after fees and shipping."""
    fee = PLATFORM_FEES[platform]
    return {
        tier: round(price * (1 - fee) - SHIPPING_COST, 2)
        for tier, price in price_tiers.items()
    }


def _compute_raw_fit_score(net_profit_balanced: float, sell_prob: float,
                           days_to_sale: int) -> float:
    """Raw fit score: expected value adjusted for time.

    Formula: (net_profit × sell_probability) / sqrt(days_to_sale)
    sqrt() penalizes slow platforms without annihilating them.
    """
    if days_to_sale <= 0 or net_profit_balanced <= 0:
        return 0.0
    return (net_profit_balanced * sell_prob) / math.sqrt(days_to_sale)


def _generate_reasoning(rec: dict, all_recs: list[dict]) -> str:
    """Generate a templated reasoning string for a recommendation."""
    platform = rec["platform"]
    category = rec["_category"]
    rank = rec["rank"]
    days = rec["estimated_days_to_sale"]
    net_balanced = rec["net_profit"]["balanced"]
    fee_pct = int(PLATFORM_FEES[platform] * 100)

    others = [r for r in all_recs if r["platform"] != platform]

    depop_note = ""
    if platform == "Depop":
        depop_note = " (listed-price proxy)"
    if platform == "Depop" and category not in THREE_WAY_CATEGORIES:
        depop_note = " (limited training data — listed-price proxy)"

    if rank == 1:
        # Find advantage over runner-up
        if others:
            runner_up = others[0]
            profit_diff = net_balanced - runner_up["net_profit"]["balanced"]
            days_diff = runner_up["estimated_days_to_sale"] - days

            parts = []
            if profit_diff > 1:
                parts.append(f"nets ${profit_diff:.0f} more than {runner_up['platform']}")
            if days_diff > 2:
                parts.append(f"estimated {days_diff} days faster")

            if parts:
                advantage = " and ".join(parts)
                return (
                    f"{category.title()}s {advantage} on {platform} "
                    f"after {fee_pct}% fee{depop_note}"
                )

        return (
            f"Best overall value for {category}s on {platform} — "
            f"${net_balanced:.0f} net profit in ~{days} days{depop_note}"
        )

    # Rank 2-3: highlight differentiator and tradeoff
    best = all_recs[0]
    profit_diff = best["net_profit"]["balanced"] - net_balanced

    if days < best["estimated_days_to_sale"]:
        days_advantage = best["estimated_days_to_sale"] - days
        return (
            f"{platform} offers faster sell-through (~{days} days, "
            f"{days_advantage} days quicker) but {fee_pct}% fee "
            f"leaves ${profit_diff:.0f} less net profit{depop_note}"
        )

    if fee_pct < int(PLATFORM_FEES[best["platform"]] * 100):
        return (
            f"{platform} has lower fees ({fee_pct}%) but estimated net profit "
            f"is ${profit_diff:.0f} less than {best['platform']}{depop_note}"
        )

    return (
        f"{platform} is a viable alternative at ${net_balanced:.0f} net profit "
        f"in ~{days} days ({fee_pct}% fee){depop_note}"
    )


def _build_worth_it(recommendations: list[dict]) -> dict:
    """Build the worth_it verdict from the best recommendation."""
    best = recommendations[0]
    best_net = best["net_profit"]["balanced"]
    hourly = best["effective_hourly_rate"]
    platform = best["platform"]

    fast_net = best["net_profit"]["fast_sale"]
    max_net = best["net_profit"]["max_revenue"]

    if hourly >= HOURLY_RATE_THRESHOLDS["worth_it"]:
        verdict = True
        explanation = (
            f"Worth listing — estimated ${fast_net:.0f}-{max_net:.0f} net profit "
            f"on {platform} after fees and shipping"
        )
    elif hourly >= HOURLY_RATE_THRESHOLDS["marginal"]:
        verdict = "marginal"
        explanation = (
            f"Marginal — estimated ${best_net:.0f} net profit on {platform}, "
            f"but effective hourly rate (${hourly:.0f}/hr) is below $20"
        )
    else:
        verdict = False
        explanation = (
            f"Not worth listing — estimated ${best_net:.0f} net profit on "
            f"{platform} yields only ${hourly:.0f}/hr effective rate"
        )

    return {
        "verdict": verdict,
        "best_net_profit": best_net,
        "best_platform": platform,
        "effective_hourly_rate": hourly,
        "explanation": explanation,
    }


def _synthesize_title(category: str, brand: str, size: str | None,
                      condition: str, color: str | None) -> str:
    """Build a pseudo-title from item attributes for title-based features.

    size and color: used for title synthesis only; not direct model features.
    """
    parts = [brand]
    if size:
        parts.append(size)
    if condition and condition != "Unknown":
        parts.append(condition)
    if color:
        parts.append(color)
    parts.append(category)
    return " ".join(parts)


