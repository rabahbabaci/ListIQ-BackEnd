# Regex patterns (replicated from notebook)
SIZE_PATTERN = re.compile(
    r"\b(?:XXS|XS|S|M|L|XL|XXL|XXXL|\d{1,2}(?:\.\d)?)\b", re.IGNORECASE
)
NWT_PATTERN = re.compile(r"\b(?:NWT|BNWT|new with tags)\b", re.IGNORECASE)


